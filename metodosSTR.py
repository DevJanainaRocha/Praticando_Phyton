mensagem = 'eu amo Codar'
        #   0123456789
print(mensagem.lower()) #imprime tudo minúsculo
print(mensagem.upper()) #imprime tudo maiúsculo
print(mensagem.capitalize()) #imprime a primeira letra em maiúsclo
print(mensagem.find('amo')) #o find localiza em qual index inicia a palavra amo
print(mensagem.find('C')) #localizando a posição index da letra C

print(mensagem.replace('a', 'e')) #o replace encontra e substitui a letra pela informeda após a virgula
print(mensagem.replace('Codar', 'programar'))